    private void btnUpdateEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateEmpActionPerformed
        // TODO add your handling code here:
        String eno=txtEmpNo.getText().trim();
        String name=txtEmpName.getText().trim();
        String sal=txtSal.getText().trim();
        if(eno.isEmpty()||name.isEmpty()||sal.isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Please input all values.","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        try
        {
            int empNo=Integer.parseInt(eno);
            double salary=Double.parseDouble(sal);
            EmpPojo obj=new EmpPojo();
            obj.setEmpNo(empNo);
            obj.setName(name);
            obj.setSal(salary);
            boolean result=EmpDAO.updateEmp(obj);
            if(result==true)
            {
                JOptionPane.showMessageDialog(null,"Record updated","Success",JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Record can not be updated !","Problem",JOptionPane.WARNING_MESSAGE);
  
            }
        }
        catch(NumberFormatException ex)
                {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(null,"Please input correct values","Error",JOptionPane.ERROR_MESSAGE);
                
                }
        catch(SQLException ex)
        {
           ex.printStackTrace();
           JOptionPane.showMessageDialog(null,"Some error occured in DB !","Error",JOptionPane.ERROR_MESSAGE);

        }
    

    }//GEN-LAST:event_btnUpdateEmpActionPerformed

